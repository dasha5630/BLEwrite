    private void displayData(String data) {
        try {
                if (data != null) {
                    String dataForString = data.substring(0, data.indexOf("\n"));
                    String dataForHex = data.substring(data.indexOf("\n") + 1).replace(" ", "");
                    if(dataForHex.equalsIgnoreCase("7E0000001F")){
                        btnPing.setBackgroundColor(Color.parseColor("#3CB043"));
                    } else if(dataForHex.contains("0001FE")){
                        byte[] bytes = hexStringToByteArray(dataForHex.substring(dataForHex.indexOf("FE") + 2, dataForHex.length() - 2));
                        int i = ByteBuffer.wrap(bytes).getInt();
                        Float result = (float)i/10;
                        mDistanceDataField.setText(String.format("%.1f", result));
                    }
                    mRawDataField.setText(data);
                }
            } catch (Exception e){

            }
    }

    public static byte[] hexStringToByteArray(String hex) {
        int l = hex.length();
        byte[] data = new byte[l / 2];
        for (int i = 0; i < l; i += 2) {
            data[i / 2] = (byte) ((Character.digit(hex.charAt(i), 16) << 4)
                    + Character.digit(hex.charAt(i + 1), 16));
        }
        return data;
    }

